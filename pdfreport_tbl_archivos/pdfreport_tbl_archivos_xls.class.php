   function NM_export_arc_archivo()
   {
         if (empty($this->arc_archivo))
         { 
             $this->Nm_ActiveSheet->getStyle($this->calc_cell($this->Xls_col) . $this->Xls_row)->getAlignment()->setHorizontal(PHPExcel_Style_Alignment::HORIZONTAL_LEFT);
             $this->Nm_ActiveSheet->setCellValue($this->calc_cell($this->Xls_col) . $this->Xls_row, ' ');
             $this->Xls_col++;
             return;
         } 
         $in_arc_archivo = $this->Ini->root . $this->Ini->path_imag_temp . "/sc_arc_archivo_" . $_SESSION['scriptcase']['sc_num_img'] . session_id() . ".jpg";
         $_SESSION['scriptcase']['sc_num_img']++;
         $arq_arc_archivo = fopen($in_arc_archivo, "w");
         if (substr($this->arc_archivo, 0, 4) == "*nm*")
         { 
             $this->arc_archivo = substr($this->arc_archivo, 4);
             $this->arc_archivo = base64_decode($this->arc_archivo);
         } 
         $img_pos_bm = strpos($this->arc_archivo, "BM");
         if (!$img_pos_bm === FALSE && $img_pos_bm == 78)
         { 
             $this->arc_archivo = substr($this->arc_archivo, $img_pos_bm);
         } 
         fwrite($arq_arc_archivo, $this->arc_archivo);
         fclose($arq_arc_archivo);
         $md5_arc_archivo  = md5($this->arc_archivo);
         if (is_file($in_arc_archivo) && !$this->Ini->Gd_missing)
         { 
             $out_arc_archivo = $this->Ini->path_imag_temp . "/sc_arc_archivo_" . $md5_arc_archivo . ".jpg";
             $tmp_arc_archivo = fopen($in_arc_archivo, "r");
             $reg_arc_archivo = fread($tmp_arc_archivo, filesize($in_arc_archivo));
             fclose($tmp_arc_archivo);
             $arq_arc_archivo = fopen($this->Ini->root . $out_arc_archivo, "w");
             fwrite($arq_arc_archivo, $reg_arc_archivo);
             fclose($arq_arc_archivo);
             $in_arc_archivo = $this->Ini->root . $out_arc_archivo;
             $sc_obj_img = new nm_trata_img($in_arc_archivo);
             $sc_obj_img->setWidth(600);
             $sc_obj_img->setHeight(700);
             $sc_obj_img->setManterAspecto(true);
             $sc_obj_img->createImg($this->Ini->root . $out_arc_archivo);
         } 
         if (is_file($in_arc_archivo))
         { 
             $sc_obj_img = new nm_trata_img($in_arc_archivo);
             $nm_image_altura  = $sc_obj_img->getHeight();
             $nm_image_largura = $sc_obj_img->getWidth();
             $objDrawing = new PHPExcel_Worksheet_Drawing();
             $objDrawing->setPath($in_arc_archivo);
             $objDrawing->setHeight($nm_image_altura);
             $col = $this->calc_cell($this->Xls_col);
             $objDrawing->setCoordinates($col . $this->Xls_row);
             $objDrawing->setWorksheet($this->Nm_ActiveSheet);
             if (!isset($this->NM_Col_din[$col]) || $this->NM_Col_din[$col] < $nm_image_largura)
             { 
                 $this->NM_Col_din[$col] = $nm_image_largura;
             } 
             if (!isset($this->NM_Row_din[$this->Xls_row]) || $this->NM_Row_din[$this->Xls_row] < $nm_image_altura)
             { 
                 $this->NM_Row_din[$this->Xls_row] = $nm_image_altura;
             } 
         } 
         else 
         { 
             $this->Nm_ActiveSheet->getStyle($this->calc_cell($this->Xls_col) . $this->Xls_row)->getAlignment()->setHorizontal(PHPExcel_Style_Alignment::HORIZONTAL_LEFT);
             $this->Nm_ActiveSheet->setCellValue($this->calc_cell($this->Xls_col) . $this->Xls_row, ' ');
         } 
